#include "libft.h"

void	ft_btree_apply_suffix(t_btree *root, void (*applyf)(t_btree*))
{
	if (root)
	{
		ft_btree_apply_suffix(root->left, applyf);
		ft_btree_apply_suffix(root->right, applyf);
		applyf(root);
	}
}
